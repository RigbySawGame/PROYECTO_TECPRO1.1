    private void EnviarFichero(){
        /*atrapar posibles errores*/
        try{
            FileWriter fw;
            PrintWriter pw;
            
            //crear el archivo
            fw = new FileWriter("D:\\PROYECTO_TECPRO\\src\\Data\\TrabajadoresBoleteria.txt", true);
            //escribir el archivo
            pw = new PrintWriter(fw);
            //enviar datos al archivo
            TRABAJADORES obj = new TRABAJADORES();            
            obj.setTrabUsuario(txtUsusario.getText());
            obj.setTrabPassword(txtPassword1.getText());
            
            pw.println(
                    obj.getTrabUsuario()+ "\n" + 
                    obj.getTrabPassword()
            );                            
            //liberar buffer
            pw.close();
            //envia mensaje de exito
            JOptionPane.showMessageDialog(null, "Trabajador Registrado");             
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error Fichero:\n" +
                    ex.getMessage() );
        }
        
        
    }
    
    
