    private void btnInicioSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioSesionActionPerformed
        
        FileReader fr = null;
        try {
            // TODO add your handling code here:
            int NumLineas = 0;
            int i = 0;
            String [] usuarios = null;
            String linea;
            sc = new Scanner(new File("D:\\PROYECTO_TECPRO\\src\\Data\\ClienteUserPass.txt"));
            File f = new File("D:\\PROYECTO_TECPRO\\src\\Data\\ClienteUserPass.txt");
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            try {
                while((linea = br.readLine()) != null){
                    NumLineas++;
                }
            } catch (IOException ex) {
                Logger.getLogger(INICIO_SESION_CLIENTE.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            usuarios = new String [NumLineas]; //tama침침o del arreglo
            
            //mientras haya una linea valida, que la agrege al arreglo
            while(sc.hasNextLine()){
                usuarios[i++] = sc.nextLine();  //almacenando cada linea en una posicion del arreglo **en la posicion: i
            }
            
            NumIntentos++;
            
            user = txtUser.getText();
            pass = txtPass.getText();
            
            VALIDACION_CLIENTE val = new VALIDACION_CLIENTE(); 
            val.ValidadUsuario(usuarios, user, pass, NumIntentos);
            
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(INICIO_SESION_CLIENTE.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(INICIO_SESION_CLIENTE.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        if(NumIntentos == 0)
            this.dispose();
        
        
    }//GEN-LAST:event_btnInicioSesionActionPerformed

